protected void kosong() {
    kd_barang.setText("");
    nm_brg.setText("");
    hargabeli.setText("");
    hargajual.setText("");
    jenis.setSelectedIndex(0);
}
protected void aktif() {
    kd_barang.requestFocus();
}
protected void datatable() {
    Object[] Baris = {"Kode", "Nama", "Jenis", "Harga Beli", "Harga Jual"};
    tabmode = new DefaultTableModel(null, Baris);
    tblbrg.setModel(tabmode);

    String sql = "SELECT * FROM barang WHERE kd_barang LIKE '%" + txtcari.getText() + "%' "
               + "OR nm_brg LIKE '%" + txtcari.getText() + "%'";

    try {
        Statement stat = conn.createStatement();
        ResultSet hasil = stat.executeQuery(sql);

        while (hasil.next()) {
            String[] data = {
                hasil.getString("kd_barang"),
                hasil.getString("nm_brg"),
                hasil.getString("jenis"),
                hasil.getString("hargabeli"),
                hasil.getString("hargajual")
            };
            tabmode.addRow(data);
        }
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Gagal tampil data: " + e);
    }
}

