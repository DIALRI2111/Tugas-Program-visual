    private void bsimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bsimpanActionPerformed
        // TODO add your handling code here:
        String sql = "INSERT INTO barang VALUES (?,?,?,?,?)";
    try {
        PreparedStatement stat = conn.prepareStatement(sql);
        stat.setString(1, kd_barang.getText());
        stat.setString(2, nm_brg.getText());
        stat.setString(3, jenis.getSelectedItem().toString());
        stat.setDouble(4, Double.parseDouble(hargabeli.getText()));
        stat.setDouble(5, Double.parseDouble(hargajual.getText()));

        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data berhasil disimpan");

        kosong();
        kd_barang.requestFocus();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Data gagal disimpan: " + e);
    }
    datatable();
    }//GEN-LAST:event_bsimpanActionPerformed

    private void bubahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bubahActionPerformed
        // TODO add your handling code here:
         try {
        String sql = "UPDATE barang SET nm_brg=?, jenis=?, hargabeli=?, hargajual=? WHERE kd_barang=?";
        PreparedStatement stat = conn.prepareStatement(sql);

        stat.setString(1, nm_brg.getText());
        stat.setString(2, jenis.getSelectedItem().toString());
        stat.setDouble(3, Double.parseDouble(hargabeli.getText()));
        stat.setDouble(4, Double.parseDouble(hargajual.getText()));
        stat.setString(5, kd_barang.getText());

        stat.executeUpdate();
        JOptionPane.showMessageDialog(null, "Data berhasil diubah");

        kosong();
        kd_barang.requestFocus();

    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Data gagal diubah: " + e);
    }
    datatable(); // TODO add your handling code here:

    }//GEN-LAST:event_bubahActionPerformed

    private void bhapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bhapusActionPerformed
        // TODO add your handling code here:
        int ok = JOptionPane.showConfirmDialog(null, "Hapus data?", "Konfirmasi", JOptionPane.YES_NO_OPTION);
    if (ok == 0) {
        String sql = "DELETE FROM barang WHERE kd_barang=?";
        try {
            PreparedStatement stat = conn.prepareStatement(sql);
            stat.setString(1, kd_barang.getText());

            stat.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil dihapus");

            kosong();
            kd_barang.requestFocus();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Data gagal dihapus: " + e);
        }
        datatable();
    }
    }//GEN-LAST:event_bhapusActionPerformed

